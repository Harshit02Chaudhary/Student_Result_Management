    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
        String course = (String)jComboBox1.getSelectedItem();
        String branch = (String)jComboBox2.getSelectedItem();
        String rollNo = jTextField1.getText();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fathername = jTextField3.getText();

        try{
            if(rollNo.equals("") || name.equals("") || fathername.equals("")) {
                JOptionPane.showMessageDialog(null, "Name, RollNo or Fathername no entered");
                setVisible(false);
                new addNewStudent().setVisible(true);
                return;
            }
            String rn = rollNo;
            for(int i=0; i<rn.length(); i++) {
                char ch = rn.charAt(i);
                if(ch - '0' < 0 || ch - '0' > 9) {
                    setVisible(false);
                    JOptionPane.showMessageDialog(null, "Roll No cannot have Alphabet or Special Character");
                    new addNewStudent().setVisible(true);
                    return; 
                }
            }
            String n = name;
            for(int i=0; i<n.length(); i++) {
                char ch = n.charAt(i);
                if(!Character.isAlphabetic(ch)){
                    setVisible(false);
                    JOptionPane.showMessageDialog(null, "Name cannot have Number or Special Character");
                    new addNewStudent().setVisible(true);
                    return;
                }
            }
            String fn = fathername;
            for(int i=0; i<n.length(); i++) {
                char ch = fn.charAt(i);
                if(!Character.isAlphabetic(ch)){
                    setVisible(false);
                    JOptionPane.showMessageDialog(null, "Father Name cannot have Number or Special Character");
                    new addNewStudent().setVisible(true);
                    return;
                }
            }
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","sql@1234");

            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno,course,branch,name,gender,fathername) values('"+rollNo+"', '"+course+"', '"+branch+"', '"+name+"', '"+gender+"', '"+fathername+"')");
            JOptionPane.showMessageDialog(null, "New Student Added");
            setVisible(false);
            new addNewStudent().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_submitActionPerformed

